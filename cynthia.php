<h2>Senior Counsel</h2>
<p>Cindy Meyer graduated from The College of Idaho in 1982 with an honors B.A. in political science. She graduated from the S.J. Quinney College of Law, University of Utah in 1987 where she was a William H. Leary Scholar, a moot court semi-finalist, and a member of the Utah Law Review. Cindy practiced law in Salt Lake City for 16 years with Sessions &amp; Moore, Campbell, Maack &amp; Sesssions, and Morgan, Meyer &amp; Rice (now Morgan, Minnock, Rice &amp; James), gaining extensive experience in tort, business, real property, and creditor rights litigation. She is rated AV ® Preeminent ™ by Martindale-Hubbell. In 2003, she returned to Idaho and her current practice emphasizes personal injury, medical malpractice, business and real property litigation, as well as business, employment, and real property law.</p>
