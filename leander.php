	<div id="information" class="is-typeset editable colspan12-9 colspan6-4 colspan2-2 as-grid with-gutter"><h1 class="headline">Leander L. James IV</h1>
<p>Leander James is a founding shareholder of the firm. He has extensive experience in civil trial work, achieving large settlements and verdicts. He represents sexual abuse survivors, the catastrophically injured and business and property owners in serious disputes. Mr. James was a principal architect of the $166 million dollar Northwest Jesuit settlement and his cases have established appellate law. His practice is state, regional and national in scope.</p>
<p>Past President of the Idaho Trial Lawyers Association, Mr. James is currently President of the Idaho Trial Lawyers Association Foundation, Inc. He is an appointed Chair on the Idaho State Bar Professional Conduct Board. In 2011 the Bar awarded Mr. James the prestigious Service Award for his public service.</p>
<p>Mr. James graduated from the University of Santa Clara in 1983 with a B.A. in English, emphasis in writing. Thereafter he gained a wide variety of life experiences as a school teacher in France, editor, carpenter and commercial fisherman in Alaska. Mr. James graduated from the University of Idaho College of Law in 1993. He is admitted to practice law before state and federal bars, including the Supreme Court of the United States of America (see below for list of bar admissions). Additionally, Mr. James obtains pro hac vice admission as needed to represent clients anywhere in the United States.</p>
